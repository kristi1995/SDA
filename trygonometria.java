package Zadania_ksiazka;

public class trygonometria {
    private double x;
    public trygonometria(double x){
        this.x = x;
    }
    public double sin(){
        return Math.sin(this.x);
    }
    public double cos(){
        return Math.cos(this.x);
    }
    public double tan(){
        return Math.tan(this.x);
    }
}
